// mostrar imagem anterior

function voltarImagem() {
    imagemAtual--;

    // se passae do inicio, voltar para ultima
    if (imagemAtual < 0) {
        imagemAtual = produto.imagemP.length - 1;
    }

    document.getElementById("imagemProduto").src = 
        produto.imagemP[imagemAtual];
}

// mostrar proxima imagem

function avançarImagem() {
    imagemAtual++;

    // se passar da última, volta para a primeira
    if (imagemAtual >= produto.imagemP.length) {
        imagemAtual = 0;
    }
     
    document.getElementById("imagemProduto").src = 
        produto.imagemP[imagemAtual];
}



// Função que abre a página de detalhes do produto
function verProduto(index) {

    // Redireciona para outra página passando o índice pela URL
    window.location.href = "colcha.html?id=" + index;
}

// buscador e barra de pesquisa


function iniciarBusca() {
    let texto = document.getElementById("campo").value.toLowerCase();

    let resultado = colcha.filter(item =>
        item.informaçãoValor.toLowerCase().includes(texto)) ||
        item.descrição.toLowerCase().includes(texto)

    mostragridcolcha(resultado);
    

    document.getElementById("campo").value = "";

}
