function mostragridcolcha(lista) {
    let produto = document.getElementById("grid");

    produto.innerHTML = "";
    
    lista.forEach((item, index) => {
        produto.innerHTML += ` 
        <div class= "produtos" onclick="verProduto(${index})">
            <img src="${item.imagem}"> 
            <p>${item.descrição}<p>
            <h3>${item.informaçãoValor}</h3>
            <a href="${item.botãoContato}  " > <img class= "img"  src ="../costura/contato.png" style="width: 30px; height: 20px;"> Encomendar</a>
        </div>
    `;

            
    })
}
